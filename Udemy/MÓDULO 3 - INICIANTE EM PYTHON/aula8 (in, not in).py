'''
Operadores in e not in (Está entre, Não está entre)
strings são interáveis (pode navegar item por item, usando o indice)

0 1 2 3 4 
L u c a s 
'''

nome = 'Lucas'
print(nome[0]) # Vai retornar a letra 'L' pelo número do índice

print('c' in nome) #Vai retornar True pq tem 'c' em nome 
print('not' in nome) #Vai retornar False pq nn tem 
print('---------------------')
print('not' not in nome) #Vai retornar True pq mesmo que não tenha ele inverte com o not
